package com.metanet.controller;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

import com.metanet.domain.Video;
import com.metanet.repository.VideoRepository;

import io.swagger.annotations.ApiOperation;
import io.swagger.annotations.ApiParam;

@RestController
@RequestMapping("/Info")
public class InfoController 
{
	//@Autowired
	//private InfoService service; // InfoService service
	
	@Autowired
	private VideoRepository videorepository; // videoRepository
	
	@GetMapping("/detail")
	@ApiOperation(value="해당 레시피 정보 조회",notes="레시피아이디를 통한 상세정보 조회")
	public Video detail(
			@ApiParam(value="레시피 아이디",required=true) int videoNumber 
			)
	{
		return(videorepository.findByvideoNumber(videoNumber));
	}
	
}
